// Utility to convert string location to fake coordinates near the center
export const convertLocationToCoords = (locationStr, index = 0) => {
  if (!locationStr) return defaultCenter;
  
  // Simple deterministic hash to scatter points around the center
  const hash = locationStr.split('').reduce((a, b) => {
    a = ((a << 5) - a) + b.charCodeAt(0);
    return a & a;
  }, 0);
  
  const offsetLat = (hash % 100) / 1000 - 0.05;
  const offsetLng = ((hash >> 8) % 100) / 1000 - 0.05;

  return {
    lat: defaultCenter.lat + offsetLat,
    lng: defaultCenter.lng + offsetLng
  };
};

/**
 * Calculate straight-line distance between two coordinate points using the Haversine formula.
 * @param {{ lat: number, lng: number }} coord1
 * @param {{ lat: number, lng: number }} coord2
 * @returns {number} Distance in miles, rounded to 1 decimal place
 */
export const calculateDistance = (coord1, coord2) => {
  if (!coord1 || !coord2) return null;

  const R = 3958.8; // Earth's radius in miles
  const dLat = ((coord2.lat - coord1.lat) * Math.PI) / 180;
  const dLng = ((coord2.lng - coord1.lng) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos((coord1.lat * Math.PI) / 180) *
      Math.cos((coord2.lat * Math.PI) / 180) *
      Math.sin(dLng / 2) *
      Math.sin(dLng / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  const distance = R * c;
  return Math.round(distance * 10) / 10;
};
